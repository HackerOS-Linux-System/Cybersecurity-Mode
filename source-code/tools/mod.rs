use serde::{Deserialize, Serialize};

#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
pub enum ToolCategory {
    Reconnaissance, WebApplication, Exploitation, PasswordAttacks,
    Network, Wireless, Forensics, PostExploitation,
    SocialEngineering, VulnerabilityAnalysis, Evasion, CryptoSteganography,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub enum Difficulty { Beginner, Intermediate, Advanced, Expert }

// Use a plain struct for the static catalog (no Deserialize needed for statics)
pub struct Tool {
    pub name:             &'static str,
    pub display_name:     &'static str,
    pub description:      &'static str,
    pub long_description: &'static str,
    pub icon:             &'static str,
    pub category:         ToolCategory,
    pub subcategory:      &'static str,
    pub command:          &'static str,
    pub package:          &'static str,
    pub tags:             &'static [&'static str],
    pub difficulty:       Difficulty,
    pub color:            &'static str,
    pub website:          &'static str,
    pub is_gui:           bool,
}

// Serializable version for IPC / JSON export
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ToolRecord {
    pub name:             String,
    pub display_name:     String,
    pub description:      String,
    pub long_description: String,
    pub icon:             String,
    pub category:         ToolCategory,
    pub subcategory:      String,
    pub command:          String,
    pub package:          String,
    pub tags:             Vec<String>,
    pub difficulty:       Difficulty,
    pub color:            String,
    pub website:          String,
    pub is_gui:           bool,
}

impl From<&'static Tool> for ToolRecord {
    fn from(t: &'static Tool) -> Self {
        Self {
            name:             t.name.into(),
            display_name:     t.display_name.into(),
            description:      t.description.into(),
            long_description: t.long_description.into(),
            icon:             t.icon.into(),
            category:         t.category.clone(),
            subcategory:      t.subcategory.into(),
            command:          t.command.into(),
            package:          t.package.into(),
            tags:             t.tags.iter().map(|s| s.to_string()).collect(),
            difficulty:       t.difficulty.clone(),
            color:            t.color.into(),
            website:          t.website.into(),
            is_gui:           t.is_gui,
        }
    }
}

