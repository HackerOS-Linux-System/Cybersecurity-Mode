pub fn by_category(cat: &ToolCategory) -> Vec<&'static Tool> {
    RED_TEAM_TOOLS.iter()
        .filter(|t| std::mem::discriminant(&t.category) == std::mem::discriminant(cat))
        .collect()
}

pub fn search(query: &str) -> Vec<&'static Tool> {
    let q = query.to_lowercase();
    RED_TEAM_TOOLS.iter().filter(|t| {
        t.name.to_lowercase().contains(&q)
            || t.display_name.to_lowercase().contains(&q)
            || t.description.to_lowercase().contains(&q)
            || t.tags.iter().any(|tag| tag.contains(q.as_str()))
            || t.subcategory.to_lowercase().contains(&q)
    }).collect()
}

pub fn categories() -> Vec<String> {
    let mut seen = std::collections::HashSet::new();
    RED_TEAM_TOOLS.iter()
        .filter_map(|t| { let cat = format!("{:?}", t.category); seen.insert(cat.clone()).then_some(cat) })
        .collect()
}

pub fn subcategories() -> Vec<String> {
    let mut seen = std::collections::HashSet::new();
    RED_TEAM_TOOLS.iter()
        .filter_map(|t| seen.insert(t.subcategory).then_some(t.subcategory.to_string()))
        .collect()
}
